class Solution {

  public:
    int noConseBits(int n) {
        // code here
        int bits = 0;
        for(int i=30;i>=0;i--)
        {
            if(((1<<i)&n)>0) bits++;
            else bits=0;
            if(bits==3)
                n-=(1<<i),bits=0;
        }
        return n;
    }
};



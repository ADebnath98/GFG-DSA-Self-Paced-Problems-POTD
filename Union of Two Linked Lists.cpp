class Solution
{
    public:
    struct Node* makeUnion(struct Node* head1, struct Node* head2)
    {
        set<int>un;
        while(head1!=NULL)
        {
            un.insert(head1->data);
            head1=head1->next;
        }
        while(head2!=NULL)
        {
            un.insert(head2->data);
            head2=head2->next;
        }
        
        Node* head=new Node(*un.begin());
        Node* tail=head;
        for(auto x:un)
        {
            Node *temp=new Node(x);
            tail->next=temp;
            tail=temp;
        }
        
        return head->next;
    }
};


